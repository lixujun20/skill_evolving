async def _retrieval_and_embed(pipeline: SkillEvolvingPipeline, query: str):
    """Run retrieval phase and return (skills, embedding)."""
    ret = await pipeline._phase_retrieval(query, top_k=5, threshold=0.1)
    skills = getattr(ret, "_skills", [])
    embedding = getattr(ret, "query_embedding", None)
    return skills, embedding


