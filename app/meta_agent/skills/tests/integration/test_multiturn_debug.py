# ── Breakpoint infrastructure ──────────────────────────────────────────────────

_BP_STATE   = "/tmp/llm_bp_state.json"
_BP_PAUSE   = "/tmp/llm_bp_pause"
_BP_RESUME  = "/tmp/llm_bp_resume"
_BP_ENABLED = os.environ.get("LLM_BP_ENABLED", "0") == "1"
_BP_CALL_COUNT = 0  # global counter, incremented each LLM call


async def _breakpoint_pause(
    label: str,
    messages: list,
    system_msgs: list,
    response: str,
    extra: Optional[dict] = None,
) -> None:
    """Write current LLM call state to file and block until resume signal.

    Only active when LLM_BP_ENABLED=1.
    """
    if not _BP_ENABLED:
        return

    global _BP_CALL_COUNT
    _BP_CALL_COUNT += 1

    def _get(m, key: str, default: str = "") -> str:
        if isinstance(m, dict):
            return m.get(key, default)
        return str(getattr(m, key, default) or default)

    last_user = next(
        (_get(m, "content") for m in reversed(messages) if _get(m, "role") == "user"),
        "",
    )
    tool_history = [
        {"role": _get(m, "role"), "content": _get(m, "content")[:300]}
        for m in messages
        if _get(m, "role") in ("assistant", "tool")
    ][-6:]

    sys_text = "\n".join(_get(m, "content") for m in (system_msgs or []))

    state = {
        "call_number": _BP_CALL_COUNT,
        "label": label,
        "system_text": sys_text[:3000],
        "messages_count": len(messages),
        "last_user_msg": last_user[:2000],
        "recent_tool_history": tool_history,
        "response": response[:4000],
        "extra": extra or {},
    }
    with open(_BP_STATE, "w", encoding="utf-8") as f:
        json.dump(state, f, ensure_ascii=False, indent=2)

    # ── Write /tmp/llm_bp_ns.py — loadable variable snapshot for pdb/REPL ──
    _write_ns_file(messages, system_msgs, response, extra, state)

    # ── Append to /tmp/llm_debug_log.md ─────────────────────────────────────
    _append_debug_log(state, sys_text, last_user, response)

    open(_BP_PAUSE, "w").close()

    while os.path.exists(_BP_PAUSE) and not os.path.exists(_BP_RESUME):
        await asyncio.sleep(0.3)

    for path in (_BP_PAUSE, _BP_RESUME, _BP_STATE):
        try:
            os.remove(path)
        except FileNotFoundError:
            pass


def _write_ns_file(messages: list, system_msgs: list, response: str, extra: Optional[dict], state: dict) -> None:
    """Write /tmp/llm_bp_ns.py — a Python file that can be exec()'d to load all
    breakpoint variables into a REPL namespace.

    Usage in inspector:  exec(open('/tmp/llm_bp_ns.py').read())
    """
    def _safe_repr(obj) -> str:
        try:
            return repr(obj)
        except Exception:
            return repr(str(obj))

    lines = [
        "# Auto-generated by _breakpoint_pause — exec() this in pdb/IPython to inspect",
        f"# Breakpoint #{state['call_number']}: {state['label']}",
        f"# Generated: {datetime.datetime.now().isoformat()}",
        "",
        f"call_number = {state['call_number']!r}",
        f"label = {state['label']!r}",
        f"messages_count = {state['messages_count']!r}",
        f"response = {response!r}",
        f"extra = {_safe_repr(extra)}",
        f"state = {_safe_repr(state)}",
        "",
        "# messages — raw list (dict or Message objects), may need getattr/[] access",
        f"_messages_repr = {_safe_repr(messages)}",
        "# system_msgs — raw list",
        f"_system_msgs_repr = {_safe_repr(system_msgs)}",
        "",
        "print(f'[ns loaded] BP#{call_number}: {label}')",
        "print(f'  response[:200] = {response[:200]!r}')",
    ]
    try:
        with open("/tmp/llm_bp_ns.py", "w", encoding="utf-8") as f:
            f.write("\n".join(lines) + "\n")
    except Exception as e:
        print(f"[bp] warning: failed to write ns file: {e}")


def _append_debug_log(state: dict, sys_text: str, last_user: str, response: str) -> None:
    """Append a Markdown section for this breakpoint to /tmp/llm_debug_log.md."""
    ts = datetime.datetime.now().strftime("%H:%M:%S")
    call_no = state["call_number"]
    label = state["label"]
    msgs_count = state["messages_count"]
    tool_hist = state.get("recent_tool_history", [])
    extra = state.get("extra", {})

    section_lines = [
        f"\n---\n",
        f"## 断点 #{call_no} — {label}",
        f"**时间:** {ts}  **消息数:** {msgs_count}",
    ]

    if extra.get("tool_calls_summary"):
        section_lines.append(f"\n**工具调用:** `{extra['tool_calls_summary'][:300]}`")

    if sys_text.strip():
        section_lines += [
            "\n### System Prompt (前500字)",
            "```",
            sys_text.strip()[:500],
            "```",
        ]

    if last_user.strip():
        section_lines += [
            "\n### 最后一条 User 消息 (前800字)",
            "```",
            last_user.strip()[:800],
            "```",
        ]

    if tool_hist:
        section_lines += ["\n### 最近工具调用历史"]
        for h in tool_hist:
            role = h.get("role", "?")
            content = (h.get("content") or "")[:200]
            section_lines.append(f"- **{role}**: {content}")

    section_lines += [
        "\n### LLM 响应 (前1200字)",
        "```",
        response.strip()[:1200],
        "```",
    ]

    log_path = "/tmp/llm_debug_log.md"
    # Write header if file is new or empty
    if not os.path.exists(log_path) or os.path.getsize(log_path) == 0:
        with open(log_path, "w", encoding="utf-8") as f:
            f.write("# LLM Debug Log\n\n_每次断点触发时自动追加_\n")

    try:
        with open(log_path, "a", encoding="utf-8") as f:
            f.write("\n".join(section_lines) + "\n")
    except Exception as e:
        print(f"[bp] warning: failed to write debug log: {e}")


# ── LLM call interceptor ───────────────────────────────────────────────────────

_CALL_LOG: List[dict] = []   # global per-test, reset in setup


def _log_llm_call(label: str, messages: list, system_msgs: list, response: str):
    """Print and record one LLM round-trip.

    messages / system_msgs may be either dicts or Message pydantic objects —
    both are handled transparently.
    """
    def _get(m, key: str, default: str = "") -> str:
        """Universal getter: works for both dict and Message pydantic objects."""
        if isinstance(m, dict):
            return m.get(key, default)
        return str(getattr(m, key, default) or default)

    divider = "=" * 80
    sys_text = "\n".join(_get(m, "content") for m in (system_msgs or []))
    user_text = "\n".join(
        _get(m, "content") for m in messages if _get(m, "role") == "user"
    )
    print(f"\n{divider}")
    print(f"  LLM CALL [{label}]")
    print(f"{divider}")
    print(f"--- SYSTEM ({len(sys_text)} chars) ---")
    print(textwrap.shorten(sys_text, width=600, placeholder=" ...[truncated]"))
    print(f"--- USER MSG ({len(user_text)} chars) ---")
    print(textwrap.shorten(user_text, width=800, placeholder=" ...[truncated]"))
    print(f"--- RESPONSE ({len(response)} chars) ---")
    print(response[:1200] if len(response) > 1200 else response)
    print(divider + "\n")
    _CALL_LOG.append({"label": label, "response": response})


