# ── Pretty-printers ────────────────────────────────────────────────────────────

def _print_planning_result(pr: Optional[PlanningResult], round_num: int):
    sep = "-" * 60
    print(f"\n{sep}")
    print(f"  Planning Result — Round {round_num}")
    print(sep)
    if pr is None:
        print("  [NONE — planning phase returned None]")
        return
    print(f"  workflow_plan ({len(pr.workflow_plan)} chars):")
    print(textwrap.indent(pr.workflow_plan[:800], "    "))
    if pr.proposed_skills:
        print(f"\n  proposed_skills ({len(pr.proposed_skills)} total):")
        for ps in pr.proposed_skills:
            print(f"    - {ps.name}: {ps.description[:80]}")
            print(f"      source: {ps.source_query[:60]!r}")
            print(textwrap.indent(ps.code_fragment[:200], "      "))
    else:
        print("  proposed_skills: [] (none extracted)")
    print(sep)


def _print_pipeline_result(result, round_num: int):
    sep = "-" * 60
    print(f"\n{sep}")
    print(f"  Pipeline Result — Round {round_num}")
    print(sep)
    phases = [
        ("retrieval", result.retrieval),
        ("executor",  result.executor),
        ("extractor", result.extractor),
        ("tester",    result.tester),
        ("commit",    result.commit),
    ]
    for name, ph in phases:
        status = "✓" if ph.ok else "✗"
        print(f"  {status} {name:<12} {ph.elapsed_s:.1f}s  "
              f"detail={ph.detail[:60]!r}  err={ph.error[:40]!r}")
    print(f"  new_skill_id: {result.new_skill_id}")
    code_len = len(result.new_skill_code) if result.new_skill_code else 0
    print(f"  new_skill_code: {code_len} chars")
    print(sep)
