@dataclass
class RetrievalMetrics:
    """Aggregated retrieval metrics over a set of queries."""
    total: int = 0
    hit_at_1: int = 0
    hit_at_5: int = 0
    query_costs: List[float] = field(default_factory=list)
    query_latencies_ms: List[float] = field(default_factory=list)

    @property
    def recall_at_1(self) -> float:
        return self.hit_at_1 / self.total if self.total else 0.0

    @property
    def recall_at_5(self) -> float:
        return self.hit_at_5 / self.total if self.total else 0.0

    @property
    def precision_at_5(self) -> float:
        """Micro-averaged: hits / (total * 5). Each query contributes 0 or 1 hit."""
        return self.hit_at_5 / (self.total * 5) if self.total else 0.0

    @property
    def f1_at_5(self) -> float:
        p, r = self.precision_at_5, self.recall_at_5
        return 2 * p * r / (p + r) if (p + r) > 0 else 0.0

    @property
    def avg_cost_usd(self) -> float:
        return sum(self.query_costs) / len(self.query_costs) if self.query_costs else 0.0

    @property
    def avg_latency_ms(self) -> float:
        return sum(self.query_latencies_ms) / len(self.query_latencies_ms) if self.query_latencies_ms else 0.0

    def print_report(self, title: str = "Retrieval Benchmark") -> None:
        print(f"\n{'='*60}")
        print(f"  {title}")
        print(f"{'='*60}")
        print(f"  Total queries  : {self.total}")
        print(f"  Recall@1       : {self.recall_at_1:.3f}  ({self.hit_at_1}/{self.total})")
        print(f"  Recall@5       : {self.recall_at_5:.3f}  ({self.hit_at_5}/{self.total})")
        print(f"  Precision@5    : {self.precision_at_5:.3f}")
        print(f"  F1@5           : {self.f1_at_5:.3f}")
        print(f"  Avg latency    : {self.avg_latency_ms:.1f} ms")
        print(f"  Avg cost/query : ${self.avg_cost_usd:.7f}")
        print(f"{'='*60}\n")


