class TestSkillsBenchSpeedAndCost:
    """Basic performance tests — no real embedding API required for speed test."""

    def test_retrieval_speed_under_limit(self, mock_db):
        """End-to-end retrieval (embedding mocked) must complete in < 3 s."""
        tasks = load_fixture(max_tasks=10)
        assert tasks, "Fixture is empty"

        retriever = SkillRetriever()
        fake_emb = _random_unit_embedding(seed=42)

        # Insert 10 skills with random embeddings
        for i, task in enumerate(tasks):
            _insert_skill(mock_db, task, _random_unit_embedding(seed=i))

        # Mock embedding API; measure wall time
        mock_resp = MagicMock()
        mock_resp.data = [MagicMock(embedding=fake_emb)]
        mock_resp.usage = MagicMock(total_tokens=10)

        with patch.object(retriever._client.embeddings, "create", return_value=mock_resp):
            t0 = time.monotonic()
            result = asyncio.get_event_loop().run_until_complete(
                retriever.retrieve_for_query(tasks[0].instruction, mock_db)
            )
            elapsed_ms = (time.monotonic() - t0) * 1000

        assert elapsed_ms < SPEED_LIMIT_MS, (
            f"Retrieval took {elapsed_ms:.0f} ms (limit {SPEED_LIMIT_MS} ms)"
        )
        print(f"\n[speed] elapsed={elapsed_ms:.1f} ms  skills_found={len(result.skills)}")

    def test_cost_per_query_under_limit(self, mock_db):
        """A single real embedding call must cost less than $0.001."""
        retriever = SkillRetriever()
        # Only measure embedding cost; skip actual DB search
        mock_manager = MagicMock()
        mock_manager.search_similar_skills.return_value = []

        result = asyncio.get_event_loop().run_until_complete(
            retriever.retrieve_for_query(
                "calculate and verify citations in a BibTeX file", mock_manager
            )
        )

        assert result.embedding_tokens > 0, "Token count not reported"
        assert result.estimated_cost_usd < COST_LIMIT_USD, (
            f"Cost ${result.estimated_cost_usd:.7f} exceeds limit ${COST_LIMIT_USD}"
        )
        print(
            f"\n[cost] tokens={result.embedding_tokens}  "
            f"cost=${result.estimated_cost_usd:.7f}  "
            f"elapsed={result.elapsed_ms:.1f} ms"
        )


class TestSkillsBenchRecall:
    """Recall tests that use real ZhipuAI embeddings for both insertion and queries.

    These tests are cheap (< $0.002 total) but require the embedding API.
    They use only the first few tasks to stay fast.
    """

    def _build_db_with_embeddings(self, tasks: List[SkillsBenchTask], mock_db) -> Dict[str, int]:
        """Insert skills with real embeddings.  Returns {task_id: skill_id}."""
        retriever = SkillRetriever()
        task_to_skill_id: Dict[str, int] = {}
        for task in tasks:
            # Embed using docstring + tags for richer representation
            embed_text = f"{task.skill_docstring}\ntags: {', '.join(task.tags)}"
            embedding = retriever.generate_embedding(embed_text)
            if embedding is None:
                pytest.skip("Embedding API unavailable")
            skill = _insert_skill(mock_db, task, embedding)
            task_to_skill_id[task.task_id] = skill.id
        return task_to_skill_id

    def test_recall_at_5_basic(self, mock_db):
        """Ground-truth skill must appear in top-5 for at least half of the queries."""
        tasks = load_fixture(max_tasks=10)
        task_to_skill_id = self._build_db_with_embeddings(tasks, mock_db)

        retriever = SkillRetriever()
        metrics = RetrievalMetrics(total=len(tasks))

        from math import sqrt

        def _cosine(a, b):
            if not a or not b:
                return 0.0
            dot = sum(x * y for x, y in zip(a, b))
            na = sqrt(sum(x * x for x in a))
            nb = sqrt(sum(y * y for y in b))
            return dot / (na * nb) if na and nb else 0.0

        def _group_name_for_skill(s):
            from sqlmodel import Session
            from app.meta_agent.skills.database.models import SkillGroup
            with Session(mock_db.engine) as session:
                g = session.get(SkillGroup, s.group_id)
                return g.name if g else str(s.group_id)

        for task in tasks:
            result = asyncio.get_event_loop().run_until_complete(
                retriever.retrieve_for_query(
                    task.instruction,
                    mock_db,
                    top_k=5,
                    similarity_threshold=0.2,
                )
            )
            retrieved_ids = {s.id for s in result.skills}
            expected_id = task_to_skill_id[task.task_id]

            # Detailed per-query logging: which skills were returned and similarity
            print(f"\n[query] task={task.task_id} expected_skill_id={expected_id} result_count={len(result.skills)}")
            for rank, s in enumerate(result.skills, start=1):
                sim_val = None
                try:
                    expected_skill_obj = mock_db.get_skill(expected_id)
                    if expected_skill_obj and expected_skill_obj.embedding and s.embedding:
                        sim_val = _cosine(expected_skill_obj.embedding, s.embedding)
                except Exception:
                    sim_val = None
                gname = _group_name_for_skill(s)
                doc_snip = (s.docstring or '')[:120].replace('\n',' ') if hasattr(s, 'docstring') else ''
                sim_str = f"{sim_val:.4f}" if sim_val is not None else "n/a"
                print(f"  rank={rank} id={s.id} group={gname} sim={sim_str} doc='{doc_snip}'")

            if result.skills and result.skills[0].id == expected_id:
                metrics.hit_at_1 += 1
            if expected_id in retrieved_ids:
                metrics.hit_at_5 += 1

            metrics.query_costs.append(result.estimated_cost_usd)
            metrics.query_latencies_ms.append(result.elapsed_ms)

        metrics.print_report("SkillsBench Recall Test (10 tasks)")

        assert metrics.recall_at_5 >= RECALL_AT_5_MIN, (
            f"Recall@5 = {metrics.recall_at_5:.3f} < minimum {RECALL_AT_5_MIN:.3f}\n"
            f"Misses: {[t.task_id for t in tasks if task_to_skill_id[t.task_id] not in {s.id for s in asyncio.get_event_loop().run_until_complete(retriever.retrieve_for_query(t.instruction, mock_db, top_k=5, similarity_threshold=0.2)).skills}]}"
        )


