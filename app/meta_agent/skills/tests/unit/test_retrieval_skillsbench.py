FIXTURE_PATH = Path(__file__).parent / "skillsbench_fixture.json"

# Relaxed thresholds — embedding models may not achieve perfect recall
RECALL_AT_1_MIN = 0.30   # at least 30% of tasks match at rank-1
RECALL_AT_5_MIN = 0.55   # at least 55% of tasks have their skill in top-5
SPEED_LIMIT_MS = 3000    # 3 s per query (embedding mocked)
COST_LIMIT_USD = 0.001   # $0.001 per real query


# ---------------------------------------------------------------------------
# Fixture data loading
# ---------------------------------------------------------------------------

@dataclass
class SkillsBenchTask:
    task_id: str
    category: str
    tags: List[str]
    difficulty: str
    instruction: str
    skill_docstring: str
    skill_code: str


def load_fixture(max_tasks: Optional[int] = None) -> List[SkillsBenchTask]:
    """Load tasks from the generated JSON fixture file."""
    with open(FIXTURE_PATH) as f:
        data = json.load(f)
    tasks = [SkillsBenchTask(**d) for d in data]
    if max_tasks:
        tasks = tasks[:max_tasks]
    return tasks


# ---------------------------------------------------------------------------
# DB insertion helpers
# ---------------------------------------------------------------------------

def _insert_skill(
    manager,
    task: SkillsBenchTask,
    embedding: List[float],
) -> Skill:
    """Insert a SkillsBench-derived skill into the DB, return the Skill object."""
    with Session(manager.engine) as session:
        group = session.exec(
            select(SkillGroup).where(SkillGroup.name == task.task_id)
        ).first()
        if not group:
            group = SkillGroup(name=task.task_id)
            session.add(group)
            session.commit()
            session.refresh(group)

        skill = Skill(
            group_id=group.id,
            major_version=1,
            minor_version=0,
            code=task.skill_code,
            docstring=task.skill_docstring,
            interface_schema={},
            tags=task.tags,
            embedding=embedding,
        )
        session.add(skill)
        session.commit()
        session.refresh(skill)
        return skill


def _random_unit_embedding(dim: int = 1024, seed: int = 0) -> List[float]:
    """Reproducible random unit vector (no real API call needed). Always finite."""
    import math
    floats = [math.sin(seed * 12.9898 + i * 78.233) * 43758.5453 % 1.0 for i in range(dim)]
    # Replace any non-finite values
    floats = [f if math.isfinite(f) else 0.5 for f in floats]
    magnitude = sum(x * x for x in floats) ** 0.5 or 1.0
    return [x / magnitude for x in floats]


