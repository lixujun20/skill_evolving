"""Skills retrieval benchmark tests using SkillsBench task-query pairs.

Test suite structure:
  - Speed test: verify retrieval latency < 3s with mocked embedding
  - Cost test: verify single real embedding call < $0.001
  - Recall@k test: real embeddings inserted; check ground-truth skill retrieved in top-k
  - P/R/F1 report: precision/recall/F1 over full fixture subset

Non-LLM tests: call the real ZhipuAI embedding API but NOT any LLM reasoner.
They are cheap (< $0.001/run total) and fast enough for CI.

Fixture: app/meta_agent/skills/tests/skillsbench_fixture.json
  Generated by: scripts/generate_skillsbench_fixture.py
"""

