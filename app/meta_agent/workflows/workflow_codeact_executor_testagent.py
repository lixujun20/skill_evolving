        self.memory.add_message(Message.tool_message(
            content=result,
            tool_call_id=tool_call_id,
            name=command.function.name,
        ))
        # Hack to terminate after reporting error
        original_command.function.name = Terminate.model_fields['name'].default
        original_command.function.arguments = json.dumps({"status": "failure"})
        return {'result': result, 'skip': True}

