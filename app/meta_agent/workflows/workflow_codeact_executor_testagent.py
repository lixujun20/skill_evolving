    async def handle_workflow_error_reporter(self, command: ToolCall) -> BaseTool:
        try:
            arguments = json.loads(command.function.arguments)
        except Exception as e:
            self.logger_instance.error(f"Failed to parse arguments for WorkflowErrorReporter: {command.function.arguments}, error: {e}")
            return {'result': f"Error: Failed to parse arguments for WorkflowErrorReporter: {command.function.arguments}, error: {e}", 'skip': True}
    
