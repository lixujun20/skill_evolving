        self.memory.add_message(Message.tool_message(
            content="Call specific error reporter tool according to failure_type.",
            tool_call_id=command.id,
            name=command.function.name,
        ))
        failure_type = arguments['failure_type']
        error_reporter_tool_map: Dict[str, BaseTool] = {
            'python_syntax_error': self.python_syntax_error_tool,
            'tool_interface_error': self.tool_interface_error_tool,
            'workflow_logic_error': self.workflow_logic_error_tool,
            'tool_runtime_error': self.tool_runtime_error_tool,
            'unexpected_tool_output': self.unexpected_tool_output_error_tool,
            'task_planning_error': self.task_planning_error_tool
        }
        error_reporter = error_reporter_tool_map[failure_type]
        response = await self.llm.ask_tool(
            messages=self.messages,
            system_msgs=(
                [Message.system_message(self.system_prompt)]
                if self.system_prompt
                else None
            ),
            timeout=10,
            tools=[error_reporter],
            tool_choice=ToolChoice.REQUIRED,
        )

        assert len(response.tool_calls) == 1
        original_command = command
        command = response.tool_calls[0]
        self.memory.add_message(Message.from_tool_calls(response.tool_calls))
        # self.cached_messages.append(self.messages[-1])
        self.logger_instance.info(f"🎯 Tool '{error_reporter.name}' completed its mission! Result: {command.function.arguments}")
        # Generate call in the specific schema of that scope type
        tool_args = json.loads(command.function.arguments or "{}")
        tool_call_id = command.id
        
        result = await error_reporter.execute(**tool_args)
        try:
            self.report = json.loads(result.output)
        except Exception as e:
            self.logger_instance.error(f"Failed to parse output of {error_reporter.name}: {result.output}, error: {e}")
